#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = -1;
	if (!s)
		return ;
	while (s[++i])
	{
		if (write(fd, (s + i), 1) == -1)
			return ;
	}
	if (write (fd, "\n", 1) == -1)
		return ;
}
